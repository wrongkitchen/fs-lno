<script type="text/javascript">
	window.lno = (window.lno) ? window.lno : {};
	window.lno.about = {};
	window.lno.about.scrollPane = $("#aboutRightContent").jScrollPane();
	window.lno.about.refreshPane = function(){
		window.lno.about.scrollPane.data('jsp').reinitialise();
	};
	window.lno.about.showHistoryPreview = function(pObj){
		$("#aboutHistory>.preview").html(_.template($("#historyPreview").html(), pObj));
		$("#aboutHistory>.listing").hide();
		$("#aboutHistory>.preview").show();
		window.lno.about.refreshPane();
	};
	window.lno.about.closeHistory = function(){
		$("#aboutHistory>.preview").empty();
		$("#aboutHistory>.listing").show();
		$("#aboutHistory>.preview").hide();
		window.lno.about.refreshPane();
	};
	window.lno.about.toggleView = function(pHide, pShow, pCallback){
		$("#aboutRightContent").animate({ left:"-20px", opacity: 0 }, function(){
			$(pHide).hide();
			$(pShow).show();
			$("#aboutRightContent").animate({ left:"0", opacity: 1 }, function(){
        		if(pCallback) pCallback();
			});
		});
	};
    $(".aboutLeftBtn.leftBtn").live("click", function(){
        $(".aboutLeftBtn.leftBtn").removeClass("active");
        $(this).addClass("active");
    });
    $(".aboutLeftBtn.leftTitle").live("click", function(){
        $(".aboutLeftBtn.leftBtn").removeClass("active");
    });
    $("#aboutPeopleView").live("click", function(){
    	$("#aboutPeople>.preview").hide();
    	$("#aboutPeople>.listing").show();
    });
</script>